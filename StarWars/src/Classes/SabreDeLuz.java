package Classes;

public class SabreDeLuz {
	private String tipoDeCristal;
	private String tipoDePunho;
	
	public SabreDeLuz(String tipoDeCristal, String tipoDePunho) {
		this.tipoDeCristal = tipoDeCristal;
		this.tipoDePunho = tipoDePunho;
	}

	public String getTipoDeCristal() {
		return tipoDeCristal;
	}

	public void setTipoDeCristal(String tipoDeCristal) {
		this.tipoDeCristal = tipoDeCristal;
	}

	public String getTipoDePunho() {
		return tipoDePunho;
	}

	public void setTipoDePunho(String tipoDePunho) {
		this.tipoDePunho = tipoDePunho;
	}
	
	
}
